
//Function with parameter + retun

public class Functionwithparameter {
    
    public static int multiply(int x, int y) { // is function me 2 parameter pass kiya x,y
        return x * y; // return me input diya x*y ye value to main func me bhejta hai
    }
    
    public static void main(String[] args) {
        int result = multiply(4, 5); 
        System.out.println(result);
    }
    
}
