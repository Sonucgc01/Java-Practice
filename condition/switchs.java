public class switchs {
    
public static void main(String[] args) {
    

    int choice = 2;

switch(choice){
    case 1:
        System.out.println("Burger Selected");
        break;
    case 2:
        System.out.println("Pizza Selected");
        break;
    case 3:
        System.out.println("Pasta Selected");
        break;
    default:
        System.out.println("Invalid Choice");
}

}
}
